<h1>Utloggad!</h1>

<p>
	Du är nu utloggad från Yibei.se. Om du fortfarande är inloggad på Facebook kan du när som helst logga in på Yibei.se utan att behöva ange ditt lösenord.
</p>

<p>
	Om du inte längre vill att ditt Facebook-konto skall vara kopplat till Yibei.se, kan du ta bort applikationen från Facebook genom att gå till inställningssidan på Facebook och därefter välja "Apps". Väl där kan du styra vilka rättigheter Yibei.se har.
</p>


